    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        if (daftarPesanan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih menu terlebih dahulu");
            return;
        }
        int konfirmasi = JOptionPane.showConfirmDialog(this, "Yakin hapus semua pesanan yang sudah ditambahkan?", "Konfirmasi", JOptionPane.YES_NO_OPTION);

        if (konfirmasi == JOptionPane.YES_OPTION) {
            daftarPesanan.clear(); // Mengosongkan pesanan
            totalHarga = 0;
            updateTampilanPesanan();
            JOptionPane.showMessageDialog(this, "Semua daftar pesanan telah dihapus.");
    }//GEN-LAST:event_hapusActionPerformed
    }
    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
        String menu = ListMenu.getSelectedValue();
        if (menu == null) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih menu terlebih dahulu");
            return;
        }
        try {
            String inputJumlah = JOptionPane.showInputDialog(this, "Masukkan jumlah porsi");
            if (inputJumlah != null && !inputJumlah.trim().isEmpty()) {
                int jumlah = Integer.parseInt(inputJumlah);

                String[] pisah = menu.split("Rp");
                String namaMenu = pisah[0].trim();

                String hargaStr = pisah[1].replace(".", "").trim();
                int hargaSatuan = Integer.parseInt(hargaStr);

                int subTotal = hargaSatuan * jumlah;
                totalHarga += subTotal;

                daftarPesanan.add("- " + namaMenu + "(" + jumlah + " porsi) = Rp" + subTotal);

                updateTampilanPesanan();
                JOptionPane.showMessageDialog(this, "Menu berhasil ditambahkan :");

            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Jumlah harus berupa angka yang valid");
        }
    }//GEN-LAST:event_tambahActionPerformed

