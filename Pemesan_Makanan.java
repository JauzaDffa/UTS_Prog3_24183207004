    private void PesanBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_PesanBtnActionPerformed
        String nomorMeja = DropdownNoMeja.getSelectedItem().toString();
        String namaPemesan = PesanField.getText();
        if (daftarPesanan.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Silahkan pilih dan tambah menu terlebih dahulu!");
            return;
        }
        if (namaPemesan.trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Harap isi nama pemesan terlebih dahulu!");
            return;
        }
        String tipePesanan = "";
        if (DineInBtn.isSelected()) {
            tipePesanan = "Dine In (Makan di Sini)";
            if (nomorMeja.equals("Pilih Meja...")) {
                JOptionPane.showMessageDialog(this, "Untuk 'Dine In', harap pilih No. Meja!");
                return; // Hentikan proses
            }
        } else if (TakeAwayBtn.isSelected()) {
            tipePesanan = "Take Away (Bungkus)";
            nomorMeja = "Take Away (Tidak pakai meja)";
        }
        String struk = "Tipe Pesanan : " + tipePesanan + "\n"
                + "Nomor Meja : " + nomorMeja + "\n"
                + "Nama Pemesan : " + namaPemesan + "\n\n"
                + "Daftar Pesanan :\n" + AreaPesanan.getText();

        JOptionPane.showMessageDialog(this, struk);
    }//GEN-LAST:event_PesanBtnActionPerformed

